function toggleLangMenu() {
    const menu = document.getElementById('lang-menu');
    menu.style.display = menu.style.display === 'block' ? 'none' : 'block';
}

document.addEventListener('click', function(event) {
    const langSelector = document.querySelector('.language-selector');
    const menu = document.getElementById('lang-menu');
    if (langSelector && !langSelector.contains(event.target)) {
        menu.style.display = 'none';
    }
});

function changeLang(lang) {
    localStorage.setItem('preferredLang', lang);
    
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    const langCodes = { en: 'EN', fr: 'FR', es: 'ES', de: 'DE', ja: 'JP' };
    document.getElementById('current-lang').textContent = langCodes[lang];
    
    document.getElementById('lang-menu').style.display = 'none';
    
    return false;
}

window.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    if (savedLang !== 'en') {
        changeLang(savedLang);
    }
    
    // Fade in page on load
    document.body.style.opacity = '1';
});

// Smooth page transitions
document.addEventListener('click', function(e) {
    const link = e.target.closest('a');
    if (link && link.href && link.href.includes(window.location.hostname) && !link.target) {
        // Check if it's an internal link (not external or #)
        if (!link.href.includes('#') && !link.href.startsWith('mailto:') && 
            !link.href.includes('blog.') && !link.href.includes('testing.') && 
            !link.href.includes('files.') && !link.href.includes('open.spotify')) {
            e.preventDefault();
            document.body.style.opacity = '0';
            setTimeout(() => {
                window.location.href = link.href;
            }, 300);
        }
    }
});

